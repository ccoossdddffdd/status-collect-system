import arrow
import random
from flask import current_app
from urllib.parse import urlencode
import hashlib

def now_ts():
    return arrow.now().float_timestamp


def ts2humanize(ts):
    return arrow.get(ts).to('Asia/Shanghai').format('YYYY-MM-DD HH:mm:ss.SSS')


def safe_number(n, default=0):
    if n is None:
        return default
    return float(n)

def md5(s):
    return hashlib.md5(s.encode('utf-8')).hexdigest()

def bind_sign(params):
    sec = current_app.config['REQUEST_SECRET']

    params['__requested_at__'] = int(now_ts())

    sign = '{}{}{}'.format(sec, urlencode(params), sec)
    params['sign'] = md5(md5(sign))
    
    return params

# 内部接口验签, 从 php 发过来的请求, 都要进行验签
def check_sign(form):
    sec = current_app.config['REQUEST_SECRET']

    attrs = {}
    for i in form:
        attrs[i] = form[i]

    sign_orig = attrs['sign']
    del attrs['sign']

    sign_str = '{}{}{}'.format(sec, urlencode(attrs), sec)
    assert sign_orig == md5(md5(sign_str)), '验签不匹配'

    return True

